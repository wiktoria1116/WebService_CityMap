package org.inputparams;

/**
 *
 * @author Adam
 */
public class AdditionParams {
    private int a;
    private int b;
    
    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }


    public AdditionParams(int a, int b) {
        this.a = a;
        this.b = b;
    }
}
